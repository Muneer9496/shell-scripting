#!/bin/bash

# what is loop??
# ---> if you want to run something for a certain number of times, we use loop

# there are two major types of loops
#   1) For loop        (when you know something to be executed n number of times, we use FOR loop)
#   2) While loop      (when you don't know something to be executed n number of times,  we use WHILE loop)


# for loop syntax :

for val in 10 20 30 40 50 ; do
    echo "value of a in the list is : $val"
done 